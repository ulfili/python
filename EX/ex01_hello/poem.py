"""EX01 Poem."""

color = input("please enter a color: ")            # ask for a color
objects = input("please enter plural noun: ")      # ask for a plural noun
activity = input("please enter an activity: ")     # ask for activity
print("Roses are", str(color) + ",")               # use str to remove leading spaces in color
print(objects, "are blue,")                        # type blue object
print("I love to", activity)                       # type an activity
print("And so will you!")                          # type end of the poem
